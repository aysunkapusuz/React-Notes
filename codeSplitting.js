// What is code splitting in React?

// When you create a React app, you will need to send your code to the browser

// This code that's send to the browser is typically called your "bundle"

// If your bundle grows quite large, it can take significant time for the browser to load up all of your code

//The time taken to load your app can have a significat impact on UX and the overall business behind the app

//So, it is a common use case to try to avoid loading a large bundle

// One solution for this is "code splitting"

// Instead of sending the browser one large bundle of code, you only send the browser a chunk of your code-base

